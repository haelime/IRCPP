#pragma once

// ANSI color codes

// Reset
#define ANSI_RESET      "\033[0m"

// Regular text
#define ANSI_BLACK      "\033[30m"
#define ANSI_RED        "\033[31m"
#define ANSI_GREEN      "\033[32m"
#define ANSI_YELLOW     "\033[33m"
#define ANSI_BLUE       "\033[34m"
#define ANSI_MAGENTA    "\033[35m"
#define ANSI_CYAN       "\033[36m"
#define ANSI_WHITE      "\033[37m"

// Regular bold text
#define ANSI_BBLACK     "\033[1;30m"
#define ANSI_BRED       "\033[1;31m"
#define ANSI_BGREEN     "\033[1;32m"
#define ANSI_BYELLOW    "\033[1;33m"
#define ANSI_BBLUE      "\033[1;34m"
#define ANSI_BMAGENTA   "\033[1;35m"
#define ANSI_BCYAN      "\033[1;36m"
#define ANSI_BWHITE     "\033[1;37m"

// Regular background
#define ANSI_BGBLACK    "\033[40m"
#define ANSI_BGRED      "\033[41m"
#define ANSI_BGGREEN    "\033[42m"
#define ANSI_BGYELLOW   "\033[43m"
#define ANSI_BGBLUE     "\033[44m"
#define ANSI_BGMAGENTA  "\033[45m"
#define ANSI_BGCYAN     "\033[46m"
#define ANSI_BGWHITE    "\033[47m"